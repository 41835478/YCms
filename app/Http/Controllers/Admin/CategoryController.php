    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $result = Category::find($id)->delete();
        if($result == true){
            return response()->json(["code"=>200,"message"=>"删除成功","data"=>[]]);
